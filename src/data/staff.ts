export function getActiveStaff(): Staff[] {
  return staff
    .filter((s) => s.is_active)
    .sort((a, b) => a.sort_order - b.sort_order);
}

export function getStaffByRole(role: string): Staff[] {
  return getActiveStaff().filter((s) => s.role === role);
}

export function getHeadmaster(): Staff | undefined {
  return getActiveStaff().find((s) => s.role === "headmaster");
}

export function getStaffBySlug(slug: string): Staff | undefined {
  return staff.find((s) => s.slug === slug && s.is_active);
}

export function getRelatedStaff(
  role: string,
  excludeId: number,
  limit: number = 4
): Staff[] {
  return getActiveStaff()
    .filter((s) => s.role === role && s.id !== excludeId)
    .slice(0, limit);
}
